const LANGUAGE_STORAGE_KEY = "polkastream.language";

const resolveInitialLanguage = (): Language => {
  if (typeof window === "undefined") return "zh-CN";

  const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (stored && stored in translations) {
    return stored as Language;
  }

  const candidates = [window.navigator.language, ...(window.navigator.languages ?? [])]
    .filter(Boolean)
    .map((item) => item.toLowerCase());

  for (const candidate of candidates) {
    if (candidate.startsWith("zh-tw") || candidate.startsWith("zh-hk") || candidate.startsWith("zh-mo")) {
      return "zh-TW";
    }
    if (candidate.startsWith("zh")) return "zh-CN";
    if (candidate.startsWith("ja")) return "ja";
    if (candidate.startsWith("ko")) return "ko";
    if (candidate.startsWith("en")) return "en";
  }

  return "zh-CN";
};

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(resolveInitialLanguage);

  useEffect(() => {
    if (typeof window === "undefined") return;
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  }, [language]);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const dictionary = translations[language] ?? translations.en;
      const template = dictionary[key] ?? translations.en[key] ?? key;

      if (!vars) return template;
      return template.replace(/\{(\w+)\}/g, (_, varKey) => {
        const value = vars[varKey];
        return value === undefined ? `{${varKey}}` : String(value);
      });
    },
    [language]
  );

  const value = useMemo(() => ({ language, setLanguage, t }), [language, setLanguage, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}

